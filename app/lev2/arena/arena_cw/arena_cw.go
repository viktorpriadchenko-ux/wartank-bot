// АренаКлановаяВойна — обёртка для ИАренаСтроение + фоновый боец.
type АренаКлановаяВойна struct {
	конт    ILocalCtx
	боец    *timed_fight.ТаймерныйБоец
	// Заглушки для ИАренаСтроение
	состояние     ИАренаСостояние
	вЛог          ИВебЛог
	времяОстат    ИВремяОстат
	продуктВремя  ISafeString
	продуктСейчас ИСтатПарам
	уровень       ИСтатПарам
}

// НовАренаКлановаяВойна создаёт арену клановой войны
func НовАренаКлановаяВойна(конт ILocalCtx) *АренаКлановаяВойна {
	лог := NewLogBuf()
	лог.Info("НовАренаКлановаяВойна(): создание\n")
	конф := timed_fight.КонфигБойца{
		ЛоббиУрл: "https://wartank.ru/cw",
		БойУрл:   "",  // Бой на той же странице
		Имя:      "CW",
	}
	сам := &АренаКлановаяВойна{
		конт:          конт,
		боец:          timed_fight.НовТаймерныйБоец(конт, конф),
		состояние:     arena_state.НовАренаСостояние(),
		вЛог:          web_log.НовВебЛог(true),
		времяОстат:    down_time.НовВремОбрат(конт, 5),
		продуктВремя:  NewSafeString(),
		продуктСейчас: lev1.НовСтатПарам("не задано"),
		уровень:       lev1.НовСтатПарам("уровень"),
	}
	return сам
}

func (сам *АренаКлановаяВойна) Пуск() {
	сам.боец.Пуск()
}

// ==== Реализация ИАренаСтроение (заглушки для совместимости) ====

func (сам *АренаКлановаяВойна) Бот() ИБот                              { return сам.конт.Get("бот").Val().(ИБот) }
func (сам *АренаКлановаяВойна) Контекст() context.Context               { return сам.конт.Ctx() }
func (сам *АренаКлановаяВойна) Отменить()                               {}
func (сам *АренаКлановаяВойна) Имя() alias.ААренаИмя                    { return "Клановая война" }
func (сам *АренаКлановаяВойна) Состояние() ИАренаСостояние               { return сам.состояние }
func (сам *АренаКлановаяВойна) ВебЛог() ИВебЛог                         { return сам.вЛог }
func (сам *АренаКлановаяВойна) СписПолучить() []string                   { return nil }
func (сам *АренаКлановаяВойна) СтрОбновить(_ []string)                   {}
func (сам *АренаКлановаяВойна) Обновить()                                {}
func (сам *АренаКлановаяВойна) Сеть() ИАренаСеть                        { return nil }
func (сам *АренаКлановаяВойна) Уровень() ИСтатПарам                     { return сам.уровень }
func (сам *АренаКлановаяВойна) ПродуктСейчас() ИСтатПарам               { return сам.продуктСейчас }
func (сам *АренаКлановаяВойна) ПродуктВремяСейчас() ISafeString         { return сам.продуктВремя }
func (сам *АренаКлановаяВойна) ОбратВремяУст(времяСек alias.АВремя)     { сам.времяОстат.Уст(времяСек) }
func (сам *АренаКлановаяВойна) ВремяОстат() ИВремяОстат                  { return сам.времяОстат }
