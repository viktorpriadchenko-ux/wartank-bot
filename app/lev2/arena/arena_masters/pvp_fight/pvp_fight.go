// package pvp_fight -- линейный PVP-бой без state machine
//
// Этот пакет реализует ВЕСЬ PVP-цикл в одном горутинном потоке:
//
//	1. Открыть /pvp → найти "Участвовать в битве" → кликнуть
//	2. Найти таймер "до начала HH:MM:SS" → time.Sleep(HH:MM:SS)
//	3. Перезагрузить страницу → проверить кнопку атаки
//	4. Запустить shot/health/manevr горутины
//	5. Ждать окончания боя (5 мин)
//	6. Повторить с шага 1
