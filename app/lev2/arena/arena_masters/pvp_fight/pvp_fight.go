// ПвпБоец -- объект, который выполняет полный PVP-цикл (регистрация → ожидание → бой → повтор)
type ПвпБоец struct {
	конт    ILocalCtx
	воркер  ИХттпВоркер
	бот     ИБот
	логин   string
	работа  bool // Запущен ли горутинный цикл
	лог     ILogBuf
}

// НовПвпБоец -- создаёт PVP-бойца (вызывать один раз при создании бота)
func НовПвпБоец(конт ILocalCtx) *ПвпБоец {
	бот := конт.Get("бот").Val().(ИБот)
	сам := &ПвпБоец{
		конт:   конт,
		воркер: бот.Сеть().ВебВоркер(),
		бот:    бот,
		логин:  бот.Имя(),
		лог:    NewLogBuf(),
	}
	return сам
}

// Пуск -- запускает PVP-цикл в фоновой горутине (вызывается из bot.пуск())
// Безопасно вызывать повторно — не создаёт дублей.
func (сам *ПвпБоец) Пуск() {
	if сам.работа {
		return
	}
	сам.работа = true
	go сам.цикл()
}

// ======== ОСНОВНОЙ ЦИКЛ ========

func (сам *ПвпБоец) цикл() {
	defer func() {
		if r := recover(); r != nil {
			fmt.Printf("[pvp %s] ПАНИКА в цикле: %v\n", сам.логин, r)
		}
		сам.работа = false
	}()

	for {
		// Проверяем контекст бота
		select {
		case <-сам.конт.Ctx().Done():
			fmt.Printf("[pvp %s] контекст бота отменён, выходим\n", сам.логин)
			return
		default:
		}

		// Шаг 1: Открыть /pvp и зарегистрироваться
		ссылкаБитвы := сам.регистрация()
		if ссылкаБитвы == "" {
			// Не получилось — подождём и попробуем снова
			time.Sleep(30 * time.Second)
			continue
		}

		// Шаг 2: Ждать таймер "до начала"
		if !сам.ждатьТаймер() {
			time.Sleep(30 * time.Second)
			continue
		}

		// Шаг 3: Бой!
		сам.бой()

		// Шаг 4: Пауза перед следующим раундом
		fmt.Printf("[pvp %s] бой завершён, пауза 30 сек перед следующим\n", сам.логин)
		time.Sleep(30 * time.Second)
	}
}

// ======== ШАГ 1: РЕГИСТРАЦИЯ ========

func (сам *ПвпБоец) регистрация() string {
	fmt.Printf("[pvp %s] открываю /pvp...\n", сам.логин)
	страница := сам.воркер.Получ("https://wartank.ru/pvp")
	if len(страница) == 0 {
		fmt.Printf("[pvp %s] пустой ответ от /pvp\n", сам.логин)
		return ""
	}

	// Ищем кнопку "Участвовать в битве"
	for _, строка := range страница {
		if strings.Contains(строка, `.ILinkListener-joinLink" class="simple-but border"><span><span>Участвовать в битве`) {
			// <a w:id="joinLink" href="pvp?45-5.ILinkListener-joinLink" class="simple-but border"><span><span>Участвовать в битве</span></span></a>
			ссылка := strings.TrimPrefix(строка, `<a w:id="joinLink" href="`)
			ссылка = strings.Split(ссылка, `" class=`)[0]
			ссылка = "https://wartank.ru/" + ссылка
			fmt.Printf("[pvp %s] кликаю 'Участвовать': %s\n", сам.логин, ссылка)
			результат := сам.воркер.Получ(ссылка)
			if len(результат) > 0 {
				fmt.Printf("[pvp %s] зарегистрирован на битву\n", сам.логин)
				return ссылка
			}
			return ""
		}
	}

	// Если кнопки "Участвовать" нет — возможно уже зарегистрированы
	for _, строка := range страница {
		if strings.Contains(строка, `до начала `) || strings.Contains(строка, `в очереди`) {
			fmt.Printf("[pvp %s] уже зарегистрирован (нашёл таймер/очередь)\n", сам.логин)
			return "already"
		}
	}

	fmt.Printf("[pvp %s] кнопка 'Участвовать' не найдена (строк=%d)\n", сам.логин, len(страница))
	return ""
}

// ======== ШАГ 2: ОЖИДАНИЕ ТАЙМЕРА ========

func (сам *ПвпБоец) ждатьТаймер() bool {
	// Перезагружаем /pvp чтобы найти таймер
	for попытка := 0; попытка < 120; попытка++ { // макс ~2 часа
		select {
		case <-сам.конт.Ctx().Done():
			return false
		default:
		}

		страница := сам.воркер.Получ("https://wartank.ru/pvp")
		if len(страница) == 0 {
			time.Sleep(30 * time.Second)
			continue
		}

		// Ищем таймер "до начала HH:MM:SS"
		for _, строка := range страница {
			if strings.Contains(строка, `<span>до начала `) {
				// <span>до начала 00:46:42 (2 993 заявок)</span>
				части := strings.Split(строка, `<span>до начала `)
				if len(части) < 2 {
					continue
				}
				времяСтр := strings.Split(части[1], ` (`)[0]
				секунды := парситьВремя(времяСтр)
				if секунды <= 0 {
					fmt.Printf("[pvp %s] таймер ≤ 0, бой должен начаться!\n", сам.логин)
					return true
				}
				fmt.Printf("[pvp %s] до начала %s (%d сек), сплю...\n", сам.логин, времяСтр, секунды)
				// Спим чуть меньше — чтобы не пропустить старт
				ожидание := time.Duration(секунды-5) * time.Second
				if ожидание < time.Second {
					ожидание = time.Second
				}
				select {
				case <-сам.конт.Ctx().Done():
					return false
				case <-time.After(ожидание):
				}
				// После сна — проверяем ещё раз (таймер мог обновиться)
				continue
			}
		}

		// Ищем признаки что бой уже идёт
		for _, строка := range страница {
			if strings.Contains(строка, `-currentControl-attack`) ||
				strings.Contains(строка, `<span>Ремкомплект</span>`) {
				fmt.Printf("[pvp %s] бой уже идёт! Переходим к сражению\n", сам.логин)
				return true
			}
		}

		// Ищем очередь
		вОчереди := false
		for _, строка := range страница {
			if strings.Contains(строка, `в очереди`) {
				вОчереди = true
				break
			}
		}
		if вОчереди {
			fmt.Printf("[pvp %s] в очереди, ждём 60 сек...\n", сам.логин)
			select {
			case <-сам.конт.Ctx().Done():
				return false
			case <-time.After(60 * time.Second):
			}
			continue
		}

		// Нет таймера, нет очереди, нет боя — непонятно
		fmt.Printf("[pvp %s] непонятное состояние страницы, ждём 30 сек\n", сам.логин)
		time.Sleep(30 * time.Second)
	}

	fmt.Printf("[pvp %s] таймаут ожидания (~2ч)\n", сам.логин)
	return false
}

// ======== ШАГ 3: РЕАЛЬНЫЙ БОЙ ========

func (сам *ПвпБоец) бой() {
	fmt.Printf("[pvp %s] === НАЧАЛО БОЯ ===\n", сам.логин)

	// Даём серверу 5 секунд на подготовку страницы боя
	time.Sleep(5 * time.Second)

	// Загружаем страницу боя НАПРЯМУЮ (минуя фильтр контрольной строки!)
	страница := сам.воркер.Получ("https://wartank.ru/pvp")
	if len(страница) == 0 {
		fmt.Printf("[pvp %s] пустая страница боя, выход\n", сам.логин)
		return
	}

	// Проверяем что на странице есть кнопка атаки
	естьБой := false
	for _, строка := range страница {
		if strings.Contains(строка, `-currentControl-attack`) ||
			strings.Contains(строка, `<span>Ремкомплект</span>`) ||
			strings.Contains(строка, `-currentControl-maneuverLink`) {
			естьБой = true
			break
		}
	}
	if !естьБой {
		fmt.Printf("[pvp %s] страница не содержит элементы боя (строк=%d), dump первых 5:\n", сам.логин, len(страница))
		for i, s := range страница {
			if i >= 5 {
				break
			}
			fmt.Printf("  [%d] %s\n", i, s)
		}
		// Попробуем ещё пару раз
		for повтор := 0; повтор < 6; повтор++ {
			time.Sleep(10 * time.Second)
			страница = сам.воркер.Получ("https://wartank.ru/pvp")
			for _, строка := range страница {
				if strings.Contains(строка, `-currentControl-attack`) {
					естьБой = true
					break
				}
			}
			if естьБой {
				fmt.Printf("[pvp %s] кнопка атаки появилась после %d повторов!\n", сам.логин, повтор+1)
				break
			}
			fmt.Printf("[pvp %s] повтор %d/6 — ещё нет кнопки атаки\n", сам.логин, повтор+1)
		}
		if !естьБой {
			fmt.Printf("[pvp %s] кнопка атаки так и не появилась, выход\n", сам.логин)
			return
		}
	}

	fmt.Printf("[pvp %s] кнопка атаки найдена! Запуск боевых горутин\n", сам.логин)

	// Создаём боевую обёртку (без фильтра контрольной строки!)
	кнтБой, фнОтмена := context.WithTimeout(сам.конт.Ctx(), 305*time.Second)
	defer фнОтмена()

	обёртка := &боеваяОбёртка{
		бот:           сам.бот,
		воркер:        сам.воркер,
		данные:        страница,
		блок:          sync.RWMutex{},
		кнт:           кнтБой,
		фнОтмена:      фнОтмена,
		логин:         сам.логин,
		еслиКонец:     NewSafeBool(),
		состояние:     arena_state.НовАренаСостояние(),
		вЛог:          web_log.НовВебЛог(true),
		времяОстат:    down_time.НовВремОбрат(сам.конт, 5),
		продуктВремя:  NewSafeString(),
		продуктСейчас: lev1.НовСтатПарам("не задано"),
		уровень:       lev1.НовСтатПарам("уровень"),
	}

	// Запускаем shot/health/manevr
	выстрел := shot.НовВыстрел(обёртка)
	здор := health.НовЗдоровье(обёртка)
	манёвр := manevr.НовМанёвр(обёртка)
	обёртка.выстрел = выстрел
	обёртка.здоровье = здор
	обёртка.манёвр = манёвр

	fmt.Printf("[pvp %s] горутины запущены, ждём окончания боя (макс 305 сек)\n", сам.логин)

	// Ждём окончания боя
	<-кнтБой.Done()

	fmt.Printf("[pvp %s] === БОЙ ЗАВЕРШЁН ===\n", сам.логин)
	сам.бот.Статистика().БитваМастеровДобавить(true) // TODO: определять исход
}

// ======== БОЕВАЯ ОБЁРТКА (реализует ИСражениеПроцесс без фильтра) ========

// боеваяОбёртка реализует ИАренаСтроение + ИСражениеПроцесс,
// но хранит HTML-строки НАПРЯМУЮ, без фильтра контрольной строки.
type боеваяОбёртка struct {
	бот           ИБот
	воркер        ИХттпВоркер
	данные        []string
	блок          sync.RWMutex
	кнт           context.Context
	фнОтмена      func()
	логин         string
	еслиКонец     ISafeBool
	выстрел       ИВыстрел
	здоровье      ИЗдоровье
	манёвр        ИМанёвр
	состояние     ИАренаСостояние
	вЛог          ИВебЛог
	времяОстат    ИВремяОстат
	продуктВремя  ISafeString
	продуктСейчас ИСтатПарам
	уровень       ИСтатПарам
	сеть          ИАренаСеть // lazy — не используется, shot.Сеть().Get() обходим
}

// --- ИАренаКонтекст ---
func (с *боеваяОбёртка) Бот() ИБот                           { return с.бот }
func (с *боеваяОбёртка) Контекст() context.Context            { return с.кнт }
func (с *боеваяОбёртка) Отменить()                            { с.фнОтмена() }
func (с *боеваяОбёртка) Имя() alias.ААренаИмя                 { return "PVP-бой" }
func (с *боеваяОбёртка) Состояние() ИАренаСостояние            { return с.состояние }
func (с *боеваяОбёртка) ВебЛог() ИВебЛог                      { return с.вЛог }

// --- ИАрена ---
func (с *боеваяОбёртка) СписПолучить() []string {
	с.блок.RLock()
	defer с.блок.RUnlock()
	return с.данные
}

func (с *боеваяОбёртка) СтрОбновить(lst []string) {
	if len(lst) == 0 {
		return
	}
	с.блок.Lock()
	defer с.блок.Unlock()
	// БЕЗ фильтра контрольной строки — принимаем любую страницу!
	с.данные = lst
}

func (с *боеваяОбёртка) Обновить() {
	lst := с.воркер.Получ("https://wartank.ru/pvp")
	if len(lst) > 0 {
		с.СтрОбновить(lst)
	}
}

func (с *боеваяОбёртка) Пуск() {} // не используется в бою

func (с *боеваяОбёртка) Сеть() ИАренаСеть {
	// shot вызывает Сеть().Get(link) для выстрела
	// Возвращаем специальную обёртку, которая делает GET напрямую
	if с.сеть == nil {
		с.сеть = &прямаяСеть{воркер: с.воркер, бот: с.бот}
	}
	return с.сеть
}

func (с *боеваяОбёртка) Уровень() ИСтатПарам { return с.уровень }

// --- ИАренаСтроение ---
func (с *боеваяОбёртка) ПродуктСейчас() ИСтатПарам       { return с.продуктСейчас }
func (с *боеваяОбёртка) ПродуктВремяСейчас() ISafeString  { return с.продуктВремя }
func (с *боеваяОбёртка) ОбратВремяУст(t alias.АВремя)     { с.времяОстат.Уст(t) }
func (с *боеваяОбёртка) ВремяОстат() ИВремяОстат           { return с.времяОстат }

// --- ИСражениеПроцесс ---
func (с *боеваяОбёртка) Манёвр() ИМанёвр         { return с.манёвр }
func (с *боеваяОбёртка) ЕслиКонец() ISafeBool     { return с.еслиКонец }
func (с *боеваяОбёртка) Выстрел() ИВыстрел        { return с.выстрел }
func (с *боеваяОбёртка) МанёврНадоУст() {
	if с.манёвр != nil {
		с.манёвр.УстНадо()
	}
}

// ======== ПРЯМАЯ СЕТЬ (для shot.Сеть().Get()) ========

// прямаяСеть делает HTTP GET напрямую через воркер, без фильтров
type прямаяСеть struct {
	воркер ИХттпВоркер
	бот    ИБот
}

func (с *прямаяСеть) Обновить() {}

func (с *прямаяСеть) Get(strLink string) Result[[]string] {
	lst := с.воркер.Получ(strLink)
	if len(lst) == 0 {
		return NewErr[[]string](fmt.Errorf("прямаяСеть.Get(): пустой ответ"))
	}
	return NewOk(lst)
}

func (с *прямаяСеть) ВебВоркер() ИХттпВоркер { return с.воркер }

// ======== УТИЛИТЫ ========

// парситьВремя парсит "HH:MM:SS" или "MM:SS" в секунды
func парситьВремя(стр string) int {
	части := strings.Split(strings.TrimSpace(стр), ":")
	всего := 0
	for i, ч := range части {
		знач, ош := strconv.Atoi(strings.TrimSpace(ч))
		if ош != nil {
			return 0
		}
		множитель := 1
		позиция := len(части) - 1 - i
		for п := 0; п < позиция; п++ {
			множитель *= 60
		}
		всего += знач * множитель
	}
	return всего
}
