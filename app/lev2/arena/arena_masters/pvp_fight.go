// ---------- ПвпБоец — главный объект PVP цикла ----------

// ПвпБоец выполняет полный PVP-цикл в одном горутинном потоке.
type ПвпБоец struct {
	конт    ILocalCtx
	воркер  ИХттпВоркер
	бот     ИБот
	логин   string
	работа  bool
}

// НовПвпБоец создаёт PVP-бойца.
func НовПвпБоец(конт ILocalCtx) *ПвпБоец {
	бот := конт.Get("бот").Val().(ИБот)
	сам := &ПвпБоец{
		конт:   конт,
		воркер: бот.Сеть().ВебВоркер(),
		бот:    бот,
		логин:  бот.Имя(),
	}
	return сам
}

// Пуск запускает PVP-цикл. Безопасен при повторном вызове.
func (сам *ПвпБоец) Пуск() {
	if сам.работа {
		return
	}
	сам.работа = true
	go сам.цикл()
}

// -------- ОСНОВНОЙ ЦИКЛ --------

func (сам *ПвпБоец) цикл() {
	defer func() {
		if r := recover(); r != nil {
			fmt.Printf("[pvp %s] ПАНИКА: %v\n", сам.логин, r)
		}
		сам.работа = false
	}()

	for {
		select {
		case <-сам.конт.Ctx().Done():
			fmt.Printf("[pvp %s] контекст закрыт, выходим\n", сам.логин)
			return
		default:
		}

		// === ШАГ 1: РЕГИСТРАЦИЯ ===
		if !сам.регистрация() {
			time.Sleep(30 * time.Second)
			continue
		}

		// === ШАГ 2: ОЖИДАНИЕ ТАЙМЕРА ===
		if !сам.ожидание() {
			time.Sleep(30 * time.Second)
			continue
		}

		// === ШАГ 3: БОЙ ===
		сам.бой()

		// Пауза перед следующим раундом
		fmt.Printf("[pvp %s] раунд завершён, пауза 30 сек\n", сам.логин)
		time.Sleep(30 * time.Second)
	}
}

// -------- ШАГ 1: РЕГИСТРАЦИЯ --------

func (сам *ПвпБоец) регистрация() bool {
	fmt.Printf("[pvp %s] → регистрация\n", сам.логин)
	стр := сам.воркер.Получ("https://wartank.ru/pvp")
	if len(стр) == 0 {
		fmt.Printf("[pvp %s] пустой ответ /pvp\n", сам.логин)
		return false
	}

	// Ищем кнопку "Участвовать в битве"
	for _, л := range стр {
		if strings.Contains(л, `ILinkListener-joinLink"`) && strings.Contains(л, `Участвовать в битве`) {
			ссылка := извлечьHref(л)
			if ссылка == "" {
				continue
			}
			полн := "https://wartank.ru/" + ссылка
			fmt.Printf("[pvp %s] кликаю «Участвовать»: %s\n", сам.логин, полн)
			рез := сам.воркер.Получ(полн)
			if len(рез) > 0 {
				fmt.Printf("[pvp %s] зарегистрирован\n", сам.логин)
				return true
			}
			return false
		}
	}

	// Кнопки нет — возможно уже зерегистрированы
	for _, л := range стр {
		if strings.Contains(л, "до начала ") || strings.Contains(л, "в очереди") {
			fmt.Printf("[pvp %s] уже в очереди/с таймером\n", сам.логин)
			return true
		}
	}

	fmt.Printf("[pvp %s] кнопка «Участвовать» не найдена (строк=%d)\n", сам.логин, len(стр))
	return false
}

// -------- ШАГ 2: ОЖИДАНИЕ ТАЙМЕРА --------

func (сам *ПвпБоец) ожидание() bool {
	fmt.Printf("[pvp %s] → ожидание таймера\n", сам.логин)

	for попытка := 0; попытка < 120; попытка++ { // макс ~2ч
		select {
		case <-сам.конт.Ctx().Done():
			return false
		default:
		}

		стр := сам.воркер.Получ("https://wartank.ru/pvp")
		if len(стр) == 0 {
			time.Sleep(30 * time.Second)
			continue
		}

		// Ищем таймер: <span>до начала HH:MM:SS (N заявок)</span>
		нашлиТаймер := false
		for _, л := range стр {
			if strings.Contains(л, "<span>до начала ") {
				секунды := извлечьТаймер(л)
				if секунды <= 0 {
					fmt.Printf("[pvp %s] таймер ≤ 0 → бой начинается!\n", сам.логин)
					return true
				}
				ожидание := секунды - 5
				if ожидание < 1 {
					ожидание = 1
				}
				fmt.Printf("[pvp %s] до начала %d сек, сплю %d сек\n", сам.логин, секунды, ожидание)
				select {
				case <-сам.конт.Ctx().Done():
					return false
				case <-time.After(time.Duration(ожидание) * time.Second):
				}
				нашлиТаймер = true
				break
			}
		}
		if нашлиТаймер {
			continue // Перезагружаем страницу после сна
		}

		// Может бой уже идёт? Проверяем СТРОГО — по кнопке стрельбы
		if страницаБояСтрого(стр) {
			fmt.Printf("[pvp %s] бой уже идёт (кнопка атаки на странице)!\n", сам.логин)
			return true
		}

		// В очереди но без таймера
		вОчереди := false
		for _, л := range стр {
			if strings.Contains(л, "в очереди") {
				вОчереди = true
				break
			}
		}
		if вОчереди {
			fmt.Printf("[pvp %s] в очереди, жду 15 сек\n", сам.логин)
			select {
			case <-сам.конт.Ctx().Done():
				return false
			case <-time.After(15 * time.Second):
			}
			continue
		}

		fmt.Printf("[pvp %s] нет таймера/очереди/боя (строк=%d), жду 15 сек\n", сам.логин, len(стр))
		дампСтраницы(сам.логин, "ожидание:неизвестно", стр)
		time.Sleep(15 * time.Second)
	}

	fmt.Printf("[pvp %s] таймаут ожидания (~2ч)\n", сам.логин)
	return false
}

// -------- ШАГ 3: БОЙ --------

func (сам *ПвпБоец) бой() {
	fmt.Printf("[pvp %s] === НАЧАЛО БОЯ ===\n", сам.логин)

	// Даём серверу 3 секунды на подготовку
	time.Sleep(3 * time.Second)

	// Загружаем страницу боя НАПРЯМУЮ (без фильтра контрольной строки!)
	стр := сам.воркер.Получ("https://wartank.ru/pvp")
	if len(стр) == 0 {
		fmt.Printf("[pvp %s] пустая страница боя\n", сам.логин)
		return
	}

	// Проверяем КОНКРЕТНУЮ кнопку атаки: -currentControl-attackRegularShellLink
	if !страницаБояСтрого(стр) {
		fmt.Printf("[pvp %s] нет кнопки атаки, пробуем ещё 12 раз (по 5 сек)\n", сам.логин)
		дампСтраницы(сам.логин, "бой:начало", стр)
		нашли := false
		for п := 1; п <= 12; п++ {
			time.Sleep(5 * time.Second)
			стр = сам.воркер.Получ("https://wartank.ru/pvp")
			if страницаБояСтрого(стр) {
				fmt.Printf("[pvp %s] кнопка атаки появилась (попытка %d)\n", сам.логин, п)
				нашли = true
				break
			}
			if п%4 == 0 {
				дампСтраницы(сам.логин, fmt.Sprintf("бой:повтор-%d", п), стр)
			}
		}
		if !нашли {
			fmt.Printf("[pvp %s] кнопка атаки не появилась, сброс\n", сам.логин)
			return
		}
	}

	fmt.Printf("[pvp %s] КНОПКА АТАКИ НАЙДЕНА! Запуск горутин\n", сам.логин)

	// Создаём контекст боя (305 секунд максимум)
	кнтБой, отменаБой := context.WithTimeout(сам.конт.Ctx(), 305*time.Second)
	defer отменаБой()

	// Создаём обёртку БЕЗ фильтра контрольной строки
	обёртка := новБоеваяОбёртка(сам.бот, сам.воркер, стр, кнтБой, отменаБой, сам.конт)

	// Запускаем shot/health/manevr — они стартуют свои горутины в конструкторе
	выстрел := shot.НовВыстрел(обёртка)
	здор := health.НовЗдоровье(обёртка)
	манёвр := manevr.НовМанёвр(обёртка)
	обёртка.выстрел = выстрел
	обёртка.здоровье = здор
	обёртка.манёвр = манёвр

	fmt.Printf("[pvp %s] горутины запущены, жду окончания (305 сек макс)\n", сам.логин)

	<-кнтБой.Done()

	fmt.Printf("[pvp %s] === БОЙ ЗАВЕРШЁН ===\n", сам.логин)
}

// ======================================================================
// боеваяОбёртка — реализует ИСражениеПроцесс БЕЗ фильтра контрольной
// строки. Shot/health/manevr embed'ят этот интерфейс.
// ======================================================================

type боеваяОбёртка struct {
	бот           ИБот
	воркер        ИХттпВоркер
	данные        []string
	блок          sync.RWMutex
	кнт           context.Context
	фнОтмена      context.CancelFunc
	конт          ILocalCtx
	еслиКонец     ISafeBool
	выстрел       ИВыстрел
	здоровье      ИЗдоровье
	манёвр        ИМанёвр
	состояние     ИАренаСостояние
	вЛог          ИВебЛог
	времяОстат    ИВремяОстат
	продуктВремя  ISafeString
	продуктСейчас ИСтатПарам
	уровень       ИСтатПарам
	сеть          ИАренаСеть
}

func новБоеваяОбёртка(
	бот ИБот,
	воркер ИХттпВоркер,
	начДанные []string,
	кнт context.Context,
	отмена context.CancelFunc,
	конт ILocalCtx,
) *боеваяОбёртка {
	return &боеваяОбёртка{
		бот:           бот,
		воркер:        воркер,
		данные:        начДанные,
		кнт:           кнт,
		фнОтмена:      отмена,
		конт:          конт,
		еслиКонец:     NewSafeBool(),
		состояние:     arena_state.НовАренаСостояние(),
		вЛог:          web_log.НовВебЛог(true),
		времяОстат:    down_time.НовВремОбрат(конт, 5),
		продуктВремя:  NewSafeString(),
		продуктСейчас: lev1.НовСтатПарам("не задано"),
		уровень:       lev1.НовСтатПарам("уровень"),
	}
}

// --- ИАренаКонтекст ---

func (о *боеваяОбёртка) Бот() ИБот                { return о.бот }
func (о *боеваяОбёртка) Контекст() context.Context { return о.кнт }
func (о *боеваяОбёртка) Отменить()                 { о.фнОтмена() }
func (о *боеваяОбёртка) Имя() alias.ААренаИмя      { return "PVP-бой" }
func (о *боеваяОбёртка) Состояние() ИАренаСостояние { return о.состояние }
func (о *боеваяОбёртка) ВебЛог() ИВебЛог           { return о.вЛог }

// --- ИАрена ---

func (о *боеваяОбёртка) СписПолучить() []string {
	о.блок.RLock()
	defer о.блок.RUnlock()
	return о.данные
}

// СтрОбновить — ключевой метод: принимает ЛЮБУЮ страницу, без фильтра!
func (о *боеваяОбёртка) СтрОбновить(lst []string) {
	if len(lst) == 0 {
		return
	}
	о.блок.Lock()
	о.данные = lst
	о.блок.Unlock()
}

// Обновить — GET /pvp, сохранить без фильтра
func (о *боеваяОбёртка) Обновить() {
	lst := о.воркер.Получ("https://wartank.ru/pvp")
	if len(lst) > 0 {
		о.СтрОбновить(lst)
	}
}

func (о *боеваяОбёртка) Пуск() {} // не используется

func (о *боеваяОбёртка) Сеть() ИАренаСеть {
	if о.сеть == nil {
		о.сеть = &прямаяСеть{
			ИБотСеть: о.бот.Сеть(),
			воркер:   о.воркер,
		}
	}
	return о.сеть
}

