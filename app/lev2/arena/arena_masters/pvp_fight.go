// ======================================================================
// прямаяСеть — реализует ИАренаСеть через прямой HTTP БЕЗ фильтров
// ======================================================================

type прямаяСеть struct {
	ИБотСеть // встраиваем реальную сеть бота (ЕслиОнлайн, Куки, ВебВоркер, Контекст, Бот)
	воркер   ИХттпВоркер
}

func (с *прямаяСеть) Обновить() {} // no-op, обновление через боеваяОбёртка.Обновить()

func (с *прямаяСеть) Get(ссылка string) Result[[]string] {
	lst := с.воркер.Получ(ссылка)
	if len(lst) == 0 {
		return NewErr[[]string](fmt.Errorf("прямаяСеть.Get(%s): пустой ответ", ссылка))
	}
	return NewOk(lst)
}

