// страницаБоя проверяет что на странице есть элементы реального боя (мягко)
func страницаБоя(стр []string) bool {
	for _, л := range стр {
		if strings.Contains(л, `attackRegularShellLink`) ||
			strings.Contains(л, `<span>Ремкомплект</span>`) ||
			strings.Contains(л, `maneuverLink`) {
			return true
		}
	}
	return false
}

// страницаБояСтрого проверяет наличие КОНКРЕТНОЙ ссылки стрельбы
func страницаБояСтрого(стр []string) bool {
	for _, л := range стр {
		if strings.Contains(л, `attackRegularShellLink`) {
			return true
		}
	}
	return false
}

// дампСтраницы выводит диагностику страницы (title, строк, и все строки с "attack/manevr/ремкомплект")
func дампСтраницы(логин, фаза string, стр []string) {
	тайтл := ""
	for _, л := range стр {
		if strings.Contains(л, "<title>") {
			тайтл = strings.TrimSpace(л)
			break
		}
	}
	fmt.Printf("[pvp %s] ДАМП[%s] строк=%d title=%q\n", логин, фаза, len(стр), тайтл)
	счёт := 0
	for i, л := range стр {
		есть := strings.Contains(л, "attack") ||
			strings.Contains(л, "maneuver") ||
			strings.Contains(л, "Маневр") ||
			strings.Contains(л, "Ремкомплект") ||
			strings.Contains(л, "ОБЫЧНЫЕ") ||
			strings.Contains(л, "в очереди") ||
			strings.Contains(л, "до начала")
		if есть {
			текст := л
			if len(текст) > 200 {
				текст = текст[:200] + "..."
			}
			fmt.Printf("  [%d] %s\n", i, текст)
			счёт++
		}
	}
	if счёт == 0 {
		fmt.Printf("  (ни одного ключевого элемента)\n")
	}
}

// извлечьHref достаёт значение href="..." из строки
func извлечьHref(строка string) string {
	idx := strings.Index(строка, `href="`)
	if idx == -1 {
		return ""
	}
	остаток := строка[idx+6:]
	конец := strings.Index(остаток, `"`)
	if конец == -1 {
		return ""
	}
	return остаток[:конец]
}

// извлечьТаймер парсит «до начала HH:MM:SS (N заявок)» → секунды
func извлечьТаймер(строка string) int {
	части := strings.Split(строка, "<span>до начала ")
	if len(части) < 2 {
		return 0
	}
	хвост := части[1]
	// "00:46:42 (2 993 заявок)</span>"
	пробел := strings.Index(хвост, " (")
	if пробел == -1 {
		// попробуем без скобок: "00:46:42</span>"
		пробел = strings.Index(хвост, "<")
	}
	if пробел == -1 {
		return 0
	}
	времяСтр := strings.TrimSpace(хвост[:пробел])
	return парситьВремя(времяСтр)
}

// парситьВремя парсит "HH:MM:SS" или "MM:SS" → секунды
func парситьВремя(стр string) int {
	части := strings.Split(strings.TrimSpace(стр), ":")
	всего := 0
	for i, ч := range части {
		знач, ош := strconv.Atoi(strings.TrimSpace(ч))
		if ош != nil {
			return 0
		}
		множитель := 1
		позиция := len(части) - 1 - i
		for п := 0; п < позиция; п++ {
			множитель *= 60
		}
		всего += знач * множитель
	}
	return всего
}
