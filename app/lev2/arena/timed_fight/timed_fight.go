// ---------- Конфигурация таймерного бойца ----------

// КонфигБойца -- параметры для создания таймерного бойца.
type КонфигБойца struct {
	ЛоббиУрл string // URL лобби-страницы: "https://wartank.ru/cw", "https://wartank.ru/dm"
	БойУрл   string // URL страницы боя (если отличается от лобби). Пусто = тот же что лобби
	Имя      string // Название арены для логов: "CW", "DM"
}

// бойУрл -- возвращает URL для загрузки страницы боя
func (к *КонфигБойца) бойУрл() string {
	if к.БойУрл != "" {
		return к.БойУрл
	}
	return к.ЛоббиУрл
}

// ---------- ТаймерныйБоец — главный объект ----------

// ТаймерныйБоец выполняет полный цикл: register → wait → fight → repeat.
type ТаймерныйБоец struct {
	конф    КонфигБойца
	конт    ILocalCtx
	воркер  ИХттпВоркер
	бот     ИБот
	логин   string
	работа  bool
}

// НовТаймерныйБоец создаёт нового бойца для указанной арены.
func НовТаймерныйБоец(конт ILocalCtx, конф КонфигБойца) *ТаймерныйБоец {
	бот := конт.Get("бот").Val().(ИБот)
	сам := &ТаймерныйБоец{
		конф:   конф,
		конт:   конт,
		воркер: бот.Сеть().ВебВоркер(),
		бот:    бот,
		логин:  бот.Имя(),
	}
	return сам
}

// Пуск запускает цикл бойца. Безопасен при повторном вызове (идемпотентный).
func (сам *ТаймерныйБоец) Пуск() {
	if сам.работа {
		return
	}
	сам.работа = true
	go сам.цикл()
}

// -------- ОСНОВНОЙ ЦИКЛ --------

func (сам *ТаймерныйБоец) цикл() {
	тег := сам.конф.Имя

	defer func() {
		if r := recover(); r != nil {
			fmt.Printf("[%s %s] ПАНИКА: %v\n", тег, сам.логин, r)
		}
		сам.работа = false
	}()

	for {
		select {
		case <-сам.конт.Ctx().Done():
			fmt.Printf("[%s %s] контекст закрыт, выходим\n", тег, сам.логин)
			return
		default:
		}

		// === ШАГ 1: РЕГИСТРАЦИЯ ===
		if !сам.регистрация() {
			time.Sleep(30 * time.Second)
			continue
		}

		// === ШАГ 2: ОЖИДАНИЕ ТАЙМЕРА ===
		if !сам.ожидание() {
			time.Sleep(30 * time.Second)
			continue
		}

		// === ШАГ 3: БОЙ ===
		сам.бой()

		// Пауза перед следующим раундом
		fmt.Printf("[%s %s] раунд завершён, пауза 30 сек\n", тег, сам.логин)
		time.Sleep(30 * time.Second)
	}
}

// -------- ШАГ 1: РЕГИСТРАЦИЯ --------

func (сам *ТаймерныйБоец) регистрация() bool {
	тег := сам.конф.Имя
	fmt.Printf("[%s %s] → регистрация (%s)\n", тег, сам.логин, сам.конф.ЛоббиУрл)
	стр := сам.воркер.Получ(сам.конф.ЛоббиУрл)
	if len(стр) == 0 {
		fmt.Printf("[%s %s] пустой ответ\n", тег, сам.логин)
		return false
	}

	// Ищем кнопку регистрации (несколько вариантов)
	for _, л := range стр {
		// Вариант 1: "Участвовать в битве" (как в PVP)
		if strings.Contains(л, `ILinkListener-joinLink"`) && strings.Contains(л, `Участвовать в битве`) {
			return сам.кликнутьСсылку(л, "Участвовать в битве")
		}
		// Вариант 2: "Взвод, подъем! В атаку!"
		if strings.Contains(л, `ILinkListener-currentOverview-apply`) && strings.Contains(л, `Взвод, подъем! В атаку!`) {
			return сам.кликнутьСсылку(л, "Взвод, подъем!")
		}
		// Вариант 3: Просто кнопка apply
		if strings.Contains(л, `ILinkListener-currentOverview-apply`) {
			return сам.кликнутьСсылку(л, "apply")
		}
	}

	// Кнопки нет — проверяем уже ли зарегистрированы
	for _, л := range стр {
		if strings.Contains(л, "до начала ") || strings.Contains(л, "в очереди") ||
			strings.Contains(л, "Вы в рядах участников") {
			fmt.Printf("[%s %s] уже зарегистрированы\n", тег, сам.логин)
			return true
		}
	}

	// Может бой уже идёт?
	if страницаБояСтрого(стр) {
		fmt.Printf("[%s %s] бой уже идёт!\n", тег, сам.логин)
		return true
	}

	fmt.Printf("[%s %s] кнопка регистрации не найдена (строк=%d)\n", тег, сам.логин, len(стр))
	дампСтраницы(тег, сам.логин, "рег:не_найдено", стр)
	return false
}

// кликнутьСсылку — извлекает href из строки и переходит по ссылке
func (сам *ТаймерныйБоец) кликнутьСсылку(строка, описание string) bool {
	тег := сам.конф.Имя
	ссылка := извлечьHref(строка)
	if ссылка == "" {
		fmt.Printf("[%s %s] не найден href в строке «%s»\n", тег, сам.логин, описание)
		return false
	}
	полн := "https://wartank.ru/" + ссылка
	fmt.Printf("[%s %s] кликаю «%s»: %s\n", тег, сам.логин, описание, полн)
	рез := сам.воркер.Получ(полн)
	if len(рез) > 0 {
		fmt.Printf("[%s %s] зарегистрирован\n", тег, сам.логин)
		return true
	}
	return false
}

// -------- ШАГ 2: ОЖИДАНИЕ ТАЙМЕРА --------

func (сам *ТаймерныйБоец) ожидание() bool {
	тег := сам.конф.Имя
	fmt.Printf("[%s %s] → ожидание таймера\n", тег, сам.логин)

	for попытка := 0; попытка < 120; попытка++ { // макс ~2ч
		select {
		case <-сам.конт.Ctx().Done():
			return false
		default:
		}

		стр := сам.воркер.Получ(сам.конф.ЛоббиУрл)
		if len(стр) == 0 {
			time.Sleep(30 * time.Second)
			continue
		}

		// Ищем таймер: <span>до начала HH:MM:SS (N заявок)</span>
		нашлиТаймер := false
		for _, л := range стр {
			if strings.Contains(л, "<span>до начала ") || strings.Contains(л, "до начала: ") {
				секунды := извлечьТаймер(л)
				if секунды <= 0 {
					fmt.Printf("[%s %s] таймер ≤ 0 → бой начинается!\n", тег, сам.логин)
					return true
				}
				ожидание := секунды - 5
				if ожидание < 1 {
					ожидание = 1
				}
				fmt.Printf("[%s %s] до начала %d сек, сплю %d сек\n", тег, сам.логин, секунды, ожидание)
				select {
				case <-сам.конт.Ctx().Done():
					return false
				case <-time.After(time.Duration(ожидание) * time.Second):
				}
				нашлиТаймер = true
				break
			}
		}
		if нашлиТаймер {
			continue // Перезагружаем страницу после сна
		}

		// Может бой уже идёт?
		if страницаБояСтрого(стр) {
			fmt.Printf("[%s %s] бой уже идёт (кнопка атаки на странице)!\n", тег, сам.логин)
			return true
		}

		// В очереди но без таймера
		вОчереди := false
		for _, л := range стр {
			if strings.Contains(л, "в очереди") || strings.Contains(л, "Вы в рядах участников") {
				вОчереди = true
				break
			}
		}
		if вОчереди {
			fmt.Printf("[%s %s] в очереди, жду 15 сек\n", тег, сам.логин)
			select {
			case <-сам.конт.Ctx().Done():
				return false
			case <-time.After(15 * time.Second):
			}
			continue
		}

		fmt.Printf("[%s %s] нет таймера/очереди/боя (строк=%d), жду 15 сек\n", тег, сам.логин, len(стр))
		дампСтраницы(тег, сам.логин, "ожидание:неизвестно", стр)
		time.Sleep(15 * time.Second)
	}

	fmt.Printf("[%s %s] таймаут ожидания (~2ч)\n", тег, сам.логин)
	return false
}

// -------- ШАГ 3: БОЙ --------

func (сам *ТаймерныйБоец) бой() {
	тег := сам.конф.Имя
	бойУрл := сам.конф.бойУрл()
	fmt.Printf("[%s %s] === НАЧАЛО БОЯ === (url=%s)\n", тег, сам.логин, бойУрл)

	// Даём серверу 3 секунды на подготовку
	time.Sleep(3 * time.Second)

	// Загружаем страницу боя НАПРЯМУЮ
	стр := сам.воркер.Получ(бойУрл)
	if len(стр) == 0 {
		fmt.Printf("[%s %s] пустая страница боя\n", тег, сам.логин)
		return
	}

	// Проверяем кнопку атаки
	if !страницаБояСтрого(стр) {
		fmt.Printf("[%s %s] нет кнопки атаки, пробуем ещё 12 раз (по 5 сек)\n", тег, сам.логин)
		дампСтраницы(тег, сам.логин, "бой:начало", стр)
		нашли := false
		for п := 1; п <= 12; п++ {
			time.Sleep(5 * time.Second)
			стр = сам.воркер.Получ(бойУрл)
			if страницаБояСтрого(стр) {
				fmt.Printf("[%s %s] кнопка атаки появилась (попытка %d)\n", тег, сам.логин, п)
				нашли = true
				break
			}
			if п%4 == 0 {
				дампСтраницы(тег, сам.логин, fmt.Sprintf("бой:повтор-%d", п), стр)
			}
		}
		if !нашли {
			fmt.Printf("[%s %s] кнопка атаки не появилась, сброс\n", тег, сам.логин)
			return
		}
	}

	fmt.Printf("[%s %s] КНОПКА АТАКИ НАЙДЕНА! Запуск горутин\n", тег, сам.логин)

	// Контекст боя (305 секунд максимум)
	кнтБой, отменаБой := context.WithTimeout(сам.конт.Ctx(), 305*time.Second)
	defer отменаБой()

	// Обёртка БЕЗ фильтра контрольной строки
	обёртка := новБоеваяОбёртка(сам.бот, сам.воркер, стр, кнтБой, отменаБой, сам.конт, тег, бойУрл)

	// Запускаем shot/health/manevr
	выстрел := shot.НовВыстрел(обёртка)
	здор := health.НовЗдоровье(обёртка)
	манёвр := manevr.НовМанёвр(обёртка)
	обёртка.выстрел = выстрел
	обёртка.здоровье = здор
	обёртка.манёвр = манёвр

	fmt.Printf("[%s %s] горутины запущены, жду окончания (305 сек макс)\n", тег, сам.логин)

	<-кнтБой.Done()

	fmt.Printf("[%s %s] === БОЙ ЗАВЕРШЁН ===\n", тег, сам.логин)
}

// ======================================================================
// боеваяОбёртка — реализует ИСражениеПроцесс БЕЗ фильтра контрольной
// строки. Shot/health/manevr используют этот интерфейс.
// ======================================================================

type боеваяОбёртка struct {
	бот           ИБот
	воркер        ИХттпВоркер
	данные        []string
	блок          sync.RWMutex
	кнт           context.Context
	фнОтмена      context.CancelFunc
	конт          ILocalCtx
	еслиКонец     ISafeBool
	выстрел       ИВыстрел
	здоровье      ИЗдоровье
	манёвр        ИМанёвр
	состояние     ИАренаСостояние
	вЛог          ИВебЛог
	времяОстат    ИВремяОстат
	продуктВремя  ISafeString
	продуктСейчас ИСтатПарам
	уровень       ИСтатПарам
	сеть          ИАренаСеть
	тег           string
	бойУрл        string
}

func новБоеваяОбёртка(
	бот ИБот,
	воркер ИХттпВоркер,
	начДанные []string,
	кнт context.Context,
	отмена context.CancelFunc,
	конт ILocalCtx,
	тег string,
	бойУрл string,
) *боеваяОбёртка {
	return &боеваяОбёртка{
		бот:           бот,
		воркер:        воркер,
		данные:        начДанные,
		кнт:           кнт,
		фнОтмена:      отмена,
		конт:          конт,
		еслиКонец:     NewSafeBool(),
		состояние:     arena_state.НовАренаСостояние(),
		вЛог:          web_log.НовВебЛог(true),
		времяОстат:    down_time.НовВремОбрат(конт, 5),
		продуктВремя:  NewSafeString(),
		продуктСейчас: lev1.НовСтатПарам("не задано"),
		уровень:       lev1.НовСтатПарам("уровень"),
		тег:           тег,
		бойУрл:        бойУрл,
	}
}

