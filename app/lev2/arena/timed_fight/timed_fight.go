// --- ИАрена ---

func (о *боеваяОбёртка) СписПолучить() []string {
	о.блок.RLock()
	defer о.блок.RUnlock()
	return о.данные
}

func (о *боеваяОбёртка) СтрОбновить(lst []string) {
	if len(lst) == 0 {
		return
	}
	о.блок.Lock()
	о.данные = lst
	о.блок.Unlock()
}

func (о *боеваяОбёртка) Обновить() {
	lst := о.воркер.Получ(о.бойУрл)
	if len(lst) > 0 {
		о.СтрОбновить(lst)
	}
}

