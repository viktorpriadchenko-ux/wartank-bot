package types

// ИБотСтатистика -- интерфейс статистики действий бота
type ИБотСтатистика interface {
	// --- Сражения (PVE) ---
	// СраженийВсего -- общее количество сражений
	СраженийВсего() int
	// СраженийПобед -- количество побед в сражениях
	СраженийПобед() int
	// СраженийПоражений -- количество поражений в сражениях
	СраженийПоражений() int
	// СраженияДобавить -- добавить результат сражения (победа = true)
	СраженияДобавить(еслиПобеда bool)

	// --- Битва мастеров (PVP) ---
	// БитвМастеровВсего -- общее количество PVP битв
	БитвМастеровВсего() int
	// БитвМастеровПобед -- количество побед в PVP битвах
	БитвМастеровПобед() int
	// БитвМастеровПоражений -- количество поражений в PVP битвах
	БитвМастеровПоражений() int
	// БитваМастеровДобавить -- добавить результат PVP битвы (победа = true)
	БитваМастеровДобавить(еслиПобеда bool)

	// --- Бой за топливо ---
	// ДуэлейТопливоВсего -- общее количество дуэлей за топливо
	ДуэлейТопливоВсего() int
	// ДуэлейТопливоПобед -- количество побед в дуэлях за топливо
	ДуэлейТопливоПобед() int
	// ДуэлейТопливоПоражений -- количество поражений в дуэлях за топливо
	ДуэлейТопливоПоражений() int
	// ДуэльТопливоДобавить -- добавить результат дуэли за топливо (победа = true)
	ДуэльТопливоДобавить(еслиПобеда bool)

	// --- Топливо ---
	// ТопливоПотрачено -- общее количество потраченного топлива
	ТопливоПотрачено() int
	// ТопливоДобавитьРасход -- добавить расход топлива
	ТопливоДобавитьРасход(количество int)

	// --- Медали ---
	// МедалейСобрано -- общее количество собранных медалей
	МедалейСобрано() int
	// МедальДобавить -- добавить собранную медаль
	МедальДобавить()

	// --- Циклы ---
	// ЦикловБота -- общее количество циклов работы бота
	ЦикловБота() int
	// ЦиклДобавить -- добавить цикл работы бота
	ЦиклДобавить()

	// --- Арена (обобщённая) ---
	// АренВсего -- общее количество посещений арен
	АренВсего() int
	// АренаДобавить -- добавить посещение арены
	АренаДобавить()
}
