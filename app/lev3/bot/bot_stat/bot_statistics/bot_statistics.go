// package bot_statistics -- статистика действий бота
package bot_statistics

import (
	"sync"
	"time"

	. "wartank/app/lev0/types"
)

// БотСтатистика -- потокобезопасная статистика действий бота
type БотСтатистика struct {
	блок sync.RWMutex

	// Сражения (PVE)
	сраженийВсего     int
	сраженийПобед     int
	сраженийПоражений int

	// Битва мастеров (PVP)
	битвМастеровВсего     int
	битвМастеровПобед     int
	битвМастеровПоражений int
	pvpСегодня            int       // кол-во PVP боёв сегодня (сбрасывается в полночь)
	pvpДатаСброса         time.Time // дата последнего сброса дневного счётчика

	// Дуэли за топливо
	дуэлейТопливоВсего     int
	дуэлейТопливоПобед     int
	дуэлейТопливоПоражений int

	// Топливо
	топливоПотрачено int

	// Медали
	медалейСобрано int

	// Циклы бота
	цикловБота int

	// Арены
	аренВсего int
}

// НовБотСтатистика -- возвращает новую статистику бота
func НовБотСтатистика() *БотСтатистика {
	сам := &БотСтатистика{}
	_ = ИБотСтатистика(сам)
	return сам
}

// --- Сражения (PVE) ---

func (сам *БотСтатистика) СраженийВсего() int {
	сам.блок.RLock()
	defer сам.блок.RUnlock()
	return сам.сраженийВсего
}

func (сам *БотСтатистика) СраженийПобед() int {
	сам.блок.RLock()
	defer сам.блок.RUnlock()
	return сам.сраженийПобед
}

func (сам *БотСтатистика) СраженийПоражений() int {
	сам.блок.RLock()
	defer сам.блок.RUnlock()
	return сам.сраженийПоражений
}

func (сам *БотСтатистика) СраженияДобавить(еслиПобеда bool) {
	сам.блок.Lock()
	defer сам.блок.Unlock()
	сам.сраженийВсего++
	if еслиПобеда {
		сам.сраженийПобед++
	} else {
		сам.сраженийПоражений++
	}
}

// --- Битва мастеров (PVP) ---

func (сам *БотСтатистика) БитвМастеровВсего() int {
	сам.блок.RLock()
	defer сам.блок.RUnlock()
	return сам.битвМастеровВсего
}

func (сам *БотСтатистика) БитвМастеровПобед() int {
	сам.блок.RLock()
	defer сам.блок.RUnlock()
	return сам.битвМастеровПобед
}

func (сам *БотСтатистика) БитвМастеровПоражений() int {
	сам.блок.RLock()
	defer сам.блок.RUnlock()
	return сам.битвМастеровПоражений
}

// сброситьЕслиНовыйДень -- сбрасывает дневной PVP счётчик если наступил новый день (вызывать под блокировкой)
func (сам *БотСтатистика) сброситьЕслиНовыйДень() {
	сегодня := time.Now().Truncate(24 * time.Hour)
	if сегодня.After(сам.pvpДатаСброса) {
		сам.pvpСегодня = 0
		сам.pvpДатаСброса = сегодня
	}
}

func (сам *БотСтатистика) БитваМастеровДобавить(еслиПобеда bool) {
	сам.блок.Lock()
	defer сам.блок.Unlock()
	сам.сброситьЕслиНовыйДень()
	сам.битвМастеровВсего++
	сам.pvpСегодня++
	if еслиПобеда {
		сам.битвМастеровПобед++
	} else {
		сам.битвМастеровПоражений++
	}
}

// ПвпСегодня -- кол-во PVP боёв за сегодня (сбрасывается в полночь)
func (сам *БотСтатистика) ПвпСегодня() int {
	сам.блок.Lock()
	defer сам.блок.Unlock()
	сам.сброситьЕслиНовыйДень()
	return сам.pvpСегодня
}

