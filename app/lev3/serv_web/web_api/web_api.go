// Возвращает полную статистику действий бота (JSON)
func (сам *ВебАпи) статистикаБота(кнт *fiber.Ctx) error {
	номер, ош := кнт.ParamsInt("number")
	if ош != nil {
		сообщ := fmt.Sprintf(`{"error":"неправильный номер бота(%v): %v"}`, номер, ош.Error())
		return кнт.SendString(сообщ)
	}
	ботНомер := АБотНомер(номер)
	бот := сам.прилож.ServBots().Get(ботНомер)
	if бот == nil {
		return кнт.SendString(`{"error":"нет такого бота"}`)
	}
	стат := бот.Статистика()
	json := fmt.Sprintf(`{`+
		`"сражения_всего":%d,"сражения_побед":%d,"сражения_поражений":%d,`+
		`"битвы_мастеров_всего":%d,"битвы_мастеров_побед":%d,"битвы_мастеров_поражений":%d,`+
		`"дуэли_топливо_всего":%d,"дуэли_топливо_побед":%d,"дуэли_топливо_поражений":%d,`+
		`"топливо_потрачено":%d,`+
		`"медалей_собрано":%d,`+
		`"циклов_бота":%d,`+
		`"арен_всего":%d`+
		`}`,
		стат.СраженийВсего(), стат.СраженийПобед(), стат.СраженийПоражений(),
		стат.БитвМастеровВсего(), стат.БитвМастеровПобед(), стат.БитвМастеровПоражений(),
		стат.ДуэлейТопливоВсего(), стат.ДуэлейТопливоПобед(), стат.ДуэлейТопливоПоражений(),
		стат.ТопливоПотрачено(),
		стат.МедалейСобрано(),
		стат.ЦикловБота(),
		стат.АренВсего(),
	)
	кнт.Set("Content-Type", "application/json; charset=utf-8")
	return кнт.SendString(json)
}
