	файбер.Post("/api/bot/:number/tank/fuel", сам.танкТопливо)
	файбер.Post("/api/bot/:number/angar/silver", сам.ангарСеребро)
	файбер.Post("/api/bot/:number/angar/gold", сам.ангарЗолото)

